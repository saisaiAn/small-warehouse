package cn.bean;

public class Attendance {
  private Integer attendanceNo;
  private Integer numberIntegrals;
  private String content;

    public Integer getAttendanceNo() {
        return attendanceNo;
    }

    public void setAttendanceNo(Integer attendanceNo) {
        this.attendanceNo = attendanceNo;
    }

    public Integer getNumberIntegrals() {
        return numberIntegrals;
    }

    public void setNumberIntegrals(Integer numberIntegrals) {
        this.numberIntegrals = numberIntegrals;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}
